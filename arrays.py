# Creating an array
arr = [1, 2, 3, 4, 5]

# Accessing elements
print("Element at index 0:", arr[0])  # Output: 1
print("Element at index 2:", arr[2])  # Output: 3

# Modifying elements
arr[3] = 10
print("Modified array:", arr)  # Output: [1, 2, 3, 10, 5]

# Iterating through the array
for element in arr:
    print(element)
